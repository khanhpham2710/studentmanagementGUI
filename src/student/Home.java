    private void courseSearchBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseSearchBtn1ActionPerformed
        if (courseStudentID1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student id doesn't exist");
        } else {
            int id = Integer.parseInt(courseStudentID1.getText());
            semester1.removeAllItems();
            if (course.getId(id)) {
                semester1.setSelectedIndex(-1);
                int semester = course.countSemester(id);
                if (semester >= 0) {
                    for (int i = 1; i <= semester + 1; i++) {
                        semester1.addItem(i + "");
                    }
                }
            }
        }
    }//GEN-LAST:event_courseSearchBtn1ActionPerformed

