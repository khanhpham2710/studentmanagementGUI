    private void saveBtn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtn3ActionPerformed
        if (!scoreStudentID2.getText().isEmpty() && !semester3.getText().isEmpty()) {
            int sid = Integer.parseInt(scoreStudentID2.getText());
            int semesterNo = Integer.parseInt(semester3.getText());
            if (!score.isSidSemesterExist(sid, semesterNo)) {
                if (isNumeric(course1Score.getText()) && isNumeric(course2Score.getText())
                        && isNumeric(course3Score.getText()) && isNumeric(course4Score.getText())) {
                    int id = score.getMax(); // Assuming score is an instance with getMax() method
                    String course1 = course12.getText();
                    String course2 = course22.getText();
                    String course3 = course32.getText();
                    String course4 = course42.getText();
                    double score1 = Double.parseDouble(course1Score.getText());
                    double score2 = Double.parseDouble(course2Score.getText());
                    double score3 = Double.parseDouble(course3Score.getText());
                    double score4 = Double.parseDouble(course4Score.getText());

                    double average = (score1 + score2 + score3 + score4) / 4;
                    double after = Double.parseDouble(new DecimalFormat("0.00").format(average));
                    score.insert(id, sid, semesterNo, course1, course2, course3, course4, score1, score2, score3, score4, after);
                    scoreTable.setModel(new DefaultTableModel(null, new Object[]{"ID", "Student ID", "Semester", "Course 1", "Score",
                        "Course 2", "Score", "Course 3", "Score", "Course 4", "Score", "Average"
                    }));
                    score.getScoreValue(scoreTable, "");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Score already added");
            }
        } else {
            JOptionPane.showMessageDialog(this, "No Student selected");
        }
    }//GEN-LAST:event_saveBtn3ActionPerformed

